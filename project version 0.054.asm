õMODEL SMALL
.STACK 100H
.DATA
L DW 0
MSG1 DB 13,10,'PLEASE ENTER A STRING:  $' 
MSG2 DB 13,10,'a. ENTER A NEW STRING $'  
MSG3 DB 13,10,'b. REVERSE THE STRING $'
MSG4 DB 13,10,'c. CHECK IF PALINDROME $'
MSG5 DB 13,10,'YOUR OPTION:  $'


.CODE
PROC GETSTRING 
PUSH BP
MOV BP, SP
MOV [L], 0
LEA DX, MSG1  ;DISPLAY MSG1 
MOV AH, 09
INT 21H

MOV CX, 20
GETCHAR:

MOV AH, 1
INT 21H
ADD [L], 2




LOOP GETCHAR:

POP BP 
ADD BP, [L]
RET 
ENDP GETSTRING
START:
MOV AX, @DATA
MOV DS, AX
A:
CALL GETSTRING
MENUE:
LEA DX, MSG2  ;DISPLAY MSG2 
MOV AH, 09
INT 21H
LEA DX, MSG3  ;DISPLAY MSG3 
MOV AH, 09
INT 21H
LEA DX, MSG4  ;DISPLAY MSG4 
MOV AH, 09
INT 21H
MOV AH, 1   ;GET OPTION A-C
INT 21H
CMP AL, 'a'
JE A
CMP AL, 'b'
JE B
CMP AL, 'c'
je c




B:
MOV BP, SP
MOV CX, [L]
REVERSE:
MOV AL, [BP + CX]


DEC CX
LOOP REVERSE
JMP MENUE
c:


MOV AX, 0

END START                              